package tp3;

public class AreaEmpresa {
	private String id;
	private int time;
	public AreaEmpresa(String id, int time) {
		this.id = id;
		this.time = time;
	}
	public String getId() {
		return id;
	}
	public int getTime() {
		return time;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
	
	
	
}
